#
# VARIAVEIS: sempre minúsculo
# variavel
# var1
# nome2
#
# VARIAVEIS COM NOME COMPOSTO: tudo minusculo separado por _
# minha_variavel
# valor_total
#
# PACOTES E MÓDULOS:
# utilizar nomes pequenos
# utilizar caracteres minusculos
#
# CLASSE: Primeira letra em maiusculo, se for nome composto CamelCase
# MinhaClasse
#
#
# FUNÇÕES OU MÓDULOS: Nomes compostos unidos por _ e sempre letras minusculos
# cadastrar_frutas()
# excluir()
#
# CONSTANTES: tudo maiusculos
#
# PARAMETROS: sempre minusculo e separado por _
# enviar(nome_do_arquivo)
#
#
# função = faz_isso()
